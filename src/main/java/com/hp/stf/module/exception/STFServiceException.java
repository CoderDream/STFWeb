package com.hp.stf.module.exception;

public class STFServiceException extends STFException {

	private static final long serialVersionUID = 1L;

	public STFServiceException() {
		super();
	}

	public STFServiceException(String message, Throwable cause) {
		super(message, cause);
	}

	public STFServiceException(String message) {
		super(message);
	}

	public STFServiceException(Throwable cause) {
		super(cause);
	}

}
